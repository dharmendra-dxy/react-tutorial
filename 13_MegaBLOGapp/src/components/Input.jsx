import React from 'react';
// import:
import { useId } from 'react';


// using HOOK: "Forward reference"
// syntax: const Input = React.forwardRef( function Input( {...props}, ref) {} )


const Input  = React.forwardRef( function Input({
    label,
    type= "text",
    className = "",
    ...props
}, ref) 
{

    const id = useId();

    return(
        
        <div className='w-full '>

            {
                label && <label  className='inline-block mb-1 pl-1' htmlFor={props.id}> 
                    {label} 
                </label>
            }

            <input 
            type={type}
            className={`${className} px-3 py-2 rounded-lg bg-white text-black outline-none focus:bg-gray-50 duration-200 border-gray-200 w-full`} 
            ref = {ref}
            {...props}
            id={id}

            />

        </div>

    )



})



export default Input